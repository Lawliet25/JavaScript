let a=10;
let b=5;

let resultado;
resultado= a+b; 
console.log("El resultado de la suma es: " + resultado);
resultado = a-b;
console.log("El resultado de la resta es: " + resultado);
resultado = a/b;
console.log("El resultado de la división es: " + resultado);
resultado = a*b;
console.log("El resultado de la multiplicación es: " + resultado);
resultado = a**b;
console.log("El resultado del exponente es: " + resultado);

//Utilizando operadores de incrememto ++ y decremento
resultado++;
console.log(resultado);

resultado--;
console.log(resultado);

//Ojo: operadores a+= es equivalente a: a= a+algo; 
// a-= es equivalente a a=a-algo; y así con los demás operadores aritméticos.